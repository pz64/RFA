/**
 * <h1>RF Analyzer - rtl-sdr source</h1>
 *
 * Module:      RtlsdrSource.kt
 * Description: Simple source of IQ sampling by reading from IQ files generated by the
 *              HackRF. Just for testing.
 *
 * @author Dennis Mantz
